    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
    String username = txtUsuario.getText().trim();
    String password = new String(jPasswordFieldContrasena.getPassword());

    // 1. Validar campos vac√≠os
    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "‚ö†Ô∏è Todos los campos son obligatorios.",
            "Campos vac√≠os", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 2. Mostrar progreso (animaci√≥n)
    jProgressBarAnimacion.setIndeterminate(true);
    btnIngresar.setEnabled(false); // Evita m√∫ltiples clics

    try {
        // 3. Encriptar contrase√±a con SHA-256
        String passwordHash = utiilidades.HashUtil.sha256(password);

        // 4. Buscar usuario en la base de datos
        datos.RepositorioUsuario repoUsuario = new datos.RepositorioUsuario();
        dominio.Usuario usuario = repoUsuario.findByUsernameAndPassword(username, passwordHash);

        if (usuario != null) {
            // ‚úÖ Login exitoso
            JOptionPane.showMessageDialog(this,
                "‚úÖ Bienvenido, " + usuario.getNombreUsuario() + ".\nRol: " + usuario.getRol().toUpperCase(),
                "Inicio de sesi√≥n exitoso", JOptionPane.INFORMATION_MESSAGE);

            // Abrir ventana principal
            new gui_principal(usuario).setVisible(true);
            this.dispose(); // Cierra la ventana de login

        } else {

            JOptionPane.showMessageDialog(this,
                "‚ùå Usuario o contrase√±a incorrectos.\nO la cuenta est√° inactiva.",
                "Error de autenticaci√≥n", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        // üõë Error de conexi√≥n a la BD
        JOptionPane.showMessageDialog(this,
            "‚ùå No se pudo conectar a la base de datos.\nVerifica tu conexi√≥n y vuelve a intentar.",
            "Error de conexi√≥n", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();

    } catch (Exception e) {
        // üõë Otro error inesperado
        JOptionPane.showMessageDialog(this,
            "‚ùå Error inesperado: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        jProgressBarAnimacion.setIndeterminate(false);
        btnIngresar.setEnabled(true);
    }
    }//GEN-LAST:event_btnIngresarActionPerformed

